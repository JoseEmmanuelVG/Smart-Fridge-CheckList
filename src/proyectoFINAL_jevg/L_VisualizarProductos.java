    private void MonocromaticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MonocromaticoActionPerformed
        // TODO add your handling code here:
        int i,j;
        int Ancho, Alto;
        Color RGB;
        int Red, Green, Blue, Mono;
        Ancho = DestinoImagen.getWidth();
        Alto = DestinoImagen.getHeight();

        for(i=0; i<Ancho; i++ )
        {
            for(j=0; j<Alto; j++ )
            {
                RGB = new Color(DestinoImagen.getRGB(i, j));
                Red = RGB.getRed();
                Green = RGB.getGreen();
                Blue = RGB.getBlue();
                Mono = (Red+Green+Blue)/3;
                if(Mono>=128)
                {
                    DestinoImagen.setRGB(i, j, (Color.WHITE).getRGB());   
                }
                else
                {
                    DestinoImagen.setRGB(j, j, (Color.BLACK).getRGB());
                }
                Imagen = new ImageIcon(DestinoImagen);
                DestinoLabel.setIcon(Imagen);
            }
        }
    }//GEN-LAST:event_MonocromaticoActionPerformed

    private void GrisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrisActionPerformed
        // TODO add your handling code here:
        int i,j;
        int Ancho, Alto;
        Color RGB, GRIS;
        int Red, Green, Blue, Gray;
        Ancho = DestinoImagen.getWidth();
        Alto = DestinoImagen.getHeight();

        for(i=0; i<Ancho; i++ )
        {
            for(j=0; j<Alto; j++ )
            {
                RGB = new Color(DestinoImagen.getRGB(i, j));
                Red = RGB.getRed();
                Green = RGB.getGreen();
                Blue = RGB.getBlue();
                Gray = (int)(((Red*0.3)+(Green*0.59)+(Blue*0.11)));
                GRIS = new Color(Gray, Gray, Gray);

                DestinoImagen.setRGB(i, j, GRIS.getRGB());
                Imagen = new ImageIcon(DestinoImagen);
                DestinoLabel.setIcon(Imagen);
            }
        } 
    }//GEN-LAST:event_GrisActionPerformed

