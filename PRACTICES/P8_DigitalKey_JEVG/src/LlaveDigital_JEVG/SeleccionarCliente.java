    public void Update(int Selected){
        
        String []NombreCuentas;
        if (Clientes.length==0)
        {
            TextoNombre.setEnabled(false);
            TextoEdad.setEnabled(false);
            BotonAgregar.setEnabled(false);
            ListaCuentas.setEnabled(false);
            BotonSeleccionar.setEnabled(false);
            
        }
        else{
            int i;
            Actual=BancoPrincipal.ObtenerCliente(Selected);
            TextoNombre.setText(Actual.ObtenerNombre());
            TextoEdad.setText(""+Actual.ObtenerEdad());
            NombreCuentas=new String[Actual.ObtenerNumCuentas()];
            for(i=0;i<Actual.ObtenerNumCuentas();i++){
                Cuenta Auxiliar;
                Auxiliar=Actual.ObtenerCuenta(i);
                NombreCuentas[i]=Auxiliar.ObtenerNumCuenta()+"     "+Auxiliar.ObtenerSaldo();
            }
            
            ListaCuentas.setModel(new javax.swing.AbstractListModel<String>() {
            String[] strings = NombreCuentas;
            public int getSize() { return strings.length; }
            public String getElementAt(int i) { return strings[i]; }
        });
            
        }
    }

