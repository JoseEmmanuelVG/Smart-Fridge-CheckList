    public String[] getClientes(){
        int i;
        String Clientes[];
        Clientes=new String[BancoPrincipal.ObtenerNumClientes()];
        for(i=0;i<Clientes.length;i++){
            Clientes[i]=BancoPrincipal.ObtenerCliente(i).ObtenerNombre();
        }
        return Clientes;
    }
    
